package practice_sd;

public class Lead {
	private int leadid;
	private String leadName;
	private String leadCourse;

	public int getLeadid() {
		return leadid;
	}

	public void setLeadid(int leadid) {
		this.leadid = leadid;
	}

	public String getLeadName() {
		return leadName;
	}

	public void setLeadName(String leadName) {
		this.leadName = leadName;
	}

	public String getLeadCourse() {
		return leadCourse;
	}

	public void setLeadCourse(String leadCourse) {
		this.leadCourse = leadCourse;
	}

}
